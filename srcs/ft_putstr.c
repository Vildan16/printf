#include "ft_header.h"

void	ft_putstr(char *s, int size, int *count)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i] && i < size)
	{
		ft_putchar(s[i], count);
		i++;
	}
}
